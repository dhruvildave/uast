    const next = str.at(i + 1) ?? "";

    if (curr === "реС") {
      arr.push("\\'");
      continue;
    }

    if (curr === "реТ") {
      arr.push("\\`");
      continue;
    }

    const val = devan─Бgar─лDataDict.get(curr) ?? curr;
    const next_val = devan─Бgar─лDataDict.get(next) ?? next;

